nome = ['Claudia', 'Raissa', 'Olivia', 'Luna', 'Renata', 'Fabi', 'Leticia']
invertido = nome[::-1] 
print(invertido)

''' 
Os dois pontos duplos (::) são usados para fatiamento de listas, 
strings e tuplas em Python. 
Isso é chamado de slicing e permite extrair partes de uma 
sequência com um controle avançado sobre início, fim e passo. '''